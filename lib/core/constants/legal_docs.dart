/// повторное согласие.
const String legalDocsVersion = '1.0';

const String legalDocsUpdatedAt = '20 апреля 2026';

/// Типы юридических документов.
enum LegalDocType {
  terms('Условия использования', 'terms'),
  privacy('Политика конфиденциальности', 'privacy');

  const LegalDocType(this.title, this.slug);
  final String title;
  final String slug;

  static LegalDocType? fromSlug(String slug) {
    for (final type in LegalDocType.values) {
      if (type.slug == slug) return type;
    }
    return null;
  }
}

/// Один раздел документа.
class LegalDocSection {
  final String heading;
  final String body;
  const LegalDocSection({required this.heading, required this.body});
}

/// Получить документ по типу.
List<LegalDocSection> legalDocContent(LegalDocType type) {
  switch (type) {
    case LegalDocType.terms:
      return _termsSections;
    case LegalDocType.privacy:
      return _privacySections;
  }
}

