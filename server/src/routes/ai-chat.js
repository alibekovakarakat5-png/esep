// ── Rate limiting (in-memory) ────────────────────────────────────────────────

const userCounts = new Map();
const MAX_PER_DAY_FREE = 10;
const MAX_PER_DAY_PAID = 100;

function checkLimit(userId, tier) {
  const today = new Date().toISOString().slice(0, 10);
  const key   = `${userId}:${today}`;
  const count = userCounts.get(key) || 0;
  const max   = tier === 'free' ? MAX_PER_DAY_FREE : MAX_PER_DAY_PAID;
  if (count >= max) return false;
  userCounts.set(key, count + 1);
  return true;
}

// ── POST /api/ai-chat ────────────────────────────────────────────────────────

router.post('/', async (req, res) => {
  if (!GROQ_API_KEY) {
    return res.status(503).json({ error: 'AI сервис временно недоступен' });
  }

  const { messages } = req.body;
  if (!messages || !Array.isArray(messages) || messages.length === 0) {
    return res.status(400).json({ error: 'Отправьте массив messages' });
  }

  // Rate limit
  const userId = req.user?.id || 'anon';
  const tier   = req.user?.tier || 'free';
  if (!checkLimit(userId, tier)) {
    return res.status(429).json({
      error: tier === 'free'
        ? `Лимит ${MAX_PER_DAY_FREE} вопросов/день на бесплатном тарифе. Перейдите на Solo для ${MAX_PER_DAY_PAID} вопросов.`
        : 'Лимит вопросов на сегодня исчерпан. Попробуйте завтра.',
    });
  }

  // Keep last 10 messages for context
  const trimmed = messages.slice(-10).map(m => ({
    role:    m.role === 'user' ? 'user' : 'assistant',
    content: String(m.content).slice(0, 2000),
  }));

  try {
    const response = await fetch(GROQ_URL, {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${GROQ_API_KEY}`,
        'Content-Type':  'application/json',
      },
      body: JSON.stringify({
        model:    GROQ_MODEL,
        messages: [{ role: 'system', content: SYSTEM_PROMPT }, ...trimmed],
        temperature:   0.3,
        max_tokens:    1500,
        top_p:         0.9,
      }),
    });

    if (!response.ok) {
      const err = await response.text();
      console.error('[ai-chat] Groq error:', response.status, err);
      return res.status(502).json({ error: 'Ошибка AI сервиса. Попробуйте позже.' });
    }

    const data = await response.json();
    const reply = data.choices?.[0]?.message?.content || 'Не удалось получить ответ.';

    res.json({
      reply,
      usage: {
        prompt_tokens:     data.usage?.prompt_tokens,
        completion_tokens: data.usage?.completion_tokens,
      },
    });
  } catch (err) {
    console.error('[ai-chat] fetch error:', err.message);
    res.status(500).json({ error: 'Ошибка соединения с AI. Попробуйте позже.' });
  }
});

